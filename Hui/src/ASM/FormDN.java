    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        // TODO add your handling code here:

        int i = tblNhanVien.getSelectedRow();
        String maNV = txtMaNhanVien.getText();
        String tenNV = txtHoTen.getText();
        String tuoi = txtTuoi.getText();
        String email = txtEmail.getText();
        String luong = txtLuong.getText();
        Integer tuoiNV = Integer.parseInt(tuoi);
        double luongNV;
        if (!luong.isEmpty()) {
            luongNV = Double.parseDouble(luong);

        } else {
            luongNV = 0;
        }

        Boolean checkmail = qlnv.checkEmail(email);

        if (maNV.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống mã");
        } else if (tuoiNV <= 16 || tuoiNV >= 55) {
            JOptionPane.showMessageDialog(this, "Tuổi phải lớn hơn 16 và nhỏ hơn 55");
        } else if (luongNV < 5000000) {
            JOptionPane.showMessageDialog(this, "Nhập lương lớn hơn 5000000");
        } else if (!checkmail) {
            JOptionPane.showMessageDialog(this, "Sai định dạng email!");
        } else {

            NhanVien nhanVien = new NhanVien(maNV, tenNV, tuoiNV, email, luongNV);

            Boolean check = qlnv.checkma(nhanVien);
            if (check) {
                int index = -1;
                for (int j = 0; j < qlnv.getListNhanVien().size(); j++) {
                    NhanVien existingNhanVien = qlnv.getListNhanVien().get(j);
                    if (existingNhanVien.getMaNV().equals(maNV)) {
                        index = j;
                        break;
                    }
                }

                if (index != -1) {
                    NhanVien existingNhanVien = qlnv.getListNhanVien().get(index);

                    if (!nhanVien.equals(existingNhanVien)) {
                        Boolean checkCapNhat = qlnv.capNhatSave(index, nhanVien);
                        if (checkCapNhat) {
                            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                            loadData(qlnv.getListNhanVien());

                        } else {
                            JOptionPane.showMessageDialog(this, "Trùng mã");

                        }
                    }
                }
            } else {
                Boolean checkAdd = qlnv.addNhanVien(nhanVien);
                if (checkAdd) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    loadData(qlnv.getListNhanVien());

                }

            }
        }

        txtRecord.setText("Record " + 1 + " of " + list.size());
    }//GEN-LAST:event_btnSaveMouseClicked

