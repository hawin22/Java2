    void loadData(ArrayList<NhanVien> list) {
        defaltTableModel = (DefaultTableModel) tblNhanVien.getModel();
        defaltTableModel.setRowCount(0);
        for (NhanVien nhanVien : list) {
            defaltTableModel.addRow(new Object[]{
                nhanVien.getMaNV(),
                nhanVien.getHoTen(),
                nhanVien.getTuoi(),
                nhanVien.getEmail(),
                nhanVien.getLuong()
            });
        }
    }

    private void clearForm() {
        txtMaNhanVien.setText("");
        txtHoTen.setText("");
        txtTuoi.setText("");
        txtEmail.setText("");
        txtLuong.setText("");
    }

    private void record(int i) {

        txtMaNhanVien.setText(list.get(i).getMaNV());
        txtHoTen.setText(list.get(i).getHoTen());
        txtTuoi.setText(String.valueOf(list.get(i).getTuoi()));
        txtEmail.setText(list.get(i).getEmail());
        txtLuong.setText(String.valueOf(list.get(i).getLuong()));
        tblNhanVien.setRowSelectionInterval(viTri, viTri);
        txtRecord.setText("Record " + (viTri + 1) + " of " + list.size());

    }

