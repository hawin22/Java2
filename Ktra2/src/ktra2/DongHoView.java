    void loadData(ArrayList<DongHo> list) {
        int tinhTien ;
        defaultTableModel = (DefaultTableModel) tblDongHo.getModel();
        defaultTableModel.setRowCount(0);
        for (DongHo dongHo : list) {
            defaultTableModel.addRow(new Object[]{
                dongHo.getTenKhachHang(),
                dongHo.getLoaiVe(),
                dongHo.getSoLuong(), 
                tinhTien = tinhTien(dongHo.getLoaiVe(), dongHo.getSoLuong())
            });
        }
    }

    public int tinhTien(String loai, int soLuong) {

        int thanhTien = 0;

        if (loai.equals("Trẻ em")) {
            thanhTien = 80000 * soLuong;
        } else {
            thanhTien = 100000 * soLuong;
        }

        return thanhTien;
    }

