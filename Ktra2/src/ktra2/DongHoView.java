    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        // TODO add your handling code here:
        int i = tblDongHo.getSelectedRow();
        Boolean check = qldh.remove(i);
        if (check) {
            JOptionPane.showMessageDialog(this, "Đã xóa thành công");
            loadData(qldh.getListDongHo());
        }

    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
//        int i = tblDongHo.getSelectedRow();
        String ten = txtTen.getText();
        String loai = cboLoai.getSelectedItem().toString();
        String so = txtSoLuong.getText();
        Integer soLuong = Integer.valueOf(so);
        DongHo dongHo = new DongHo(ten, loai, soLuong);
        Boolean check = qldh.add(dongHo);
        if (check) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadData(qldh.getListDongHo());
        } else {
            JOptionPane.showMessageDialog(this, "SL phải lớn hơn 0");
        }
    }//GEN-LAST:event_btnThemMouseClicked

