    private void tblDongHoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDongHoMouseClicked
        // TODO add your handling code here:
        int i = tblDongHo.getSelectedRow();
        String ten = (String) tblDongHo.getValueAt(i, 0);
        txtTen.setText(ten);
        String loai = (String) tblDongHo.getValueAt(i, 1);
        cboLoai.setSelectedItem(loai);
        Integer soLuong = (Integer) tblDongHo.getValueAt(i, 2);
        String so = String.valueOf(soLuong);
        txtSoLuong.setText(so);

    }//GEN-LAST:event_tblDongHoMouseClicked

